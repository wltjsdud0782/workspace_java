package array_study;

public class Array_03 {
    public static void main(String[] args) {
        String[] arr = new String[5];

        //배열의 길이(크기)
        System.out.println(arr.length);
        System.out.println();

        for (int i=0; i<arr.length; i++){
            System.out.println(arr[i]);
        }
    }
}
